public class test1 {
    public static void main(String[] args) {
        // 유니코드를 참고하여 'c' "Z"의 사칙연산을 표현해주세요
        // 소문자 c는 99 대문자 Z는 90이다
        System.out.println("두 코드의 합 : " + ('c' + 'Z'));
        System.out.println("두 코드의 빼기 : " + ('c' - 'Z'));
        System.out.println("두 코드의 곱하기 : " + ('c' * 'Z'));
        System.out.println("두 코드의 몫: " + ('c' / 'Z'));
        System.out.println("두 코드를 나누고 나머지 : " + ('c' % 'Z'));
    }
}